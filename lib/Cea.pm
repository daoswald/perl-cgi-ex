=head1 NAME

Cea - Shortcut for accessing CGI::Ex::App

=head1 SYNOPSIS

    use base qw(Cea);

=head1 DESCRIPTION

Cea is a barebones subclass of CGI::Ex::App useful as a namespace,
and also as a lazy way for accessing CGI::Ex::App.

Pronounced See-ah.



=cut
